// Helper function to get country by code
export const getCountryWithPhoneCodeByCode = (code: string): CountryWithPhoneCode | undefined => {
  return COUNTRIES_WITH_PHONE_CODES.find(country => country.code === code);
};

// Helper function to get phone code by country code
export const getPhoneCodeByCountryCode = (countryCode: string): CountryWithPhoneCode => {
  return COUNTRIES_WITH_PHONE_CODES.find(c => c.code === countryCode) || COUNTRIES_WITH_PHONE_CODES[0];
};

// Helper function to normalize dial codes for comparison
export const normalizeDialCode = (dialCode: string): string => {
  // Remove all non-numeric characters except the initial +
  return dialCode.replace(/[^+\d]/g, '');
};